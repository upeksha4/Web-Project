                        <p>The Glif, our first product, represented a new way of approaching consumer products, and it wouldn't have been possible without a few thousand people who believed in us. Launched on Kickstarter in 2010, the Glif was the first consumer tech project to reach such a high level of funding and acclaim on the site. Exceeding our initial goal by over 10x, Studio Neat, as a company, was born. In the years since we have updated the Glif a few times, including an adjustable version that will work with any smartphone.</p>
